package conditions;

public class NestedElse {

	public static void main(String[] args) {


		int day=2;
		
		if(day==1) 
		{
			System.out.println("sunday");
		}
		else if(day==2)
		{
			System.out.println("monday");
			
		}
		else if(day==3)
		{
			System.out.println("tuesday");
		}
		else if(day==4)
		{
			System.out.println("tuesday");
		}
		else if(day==5)
		{
			System.out.println("tuesday");
		}
		else if(day==6)
		{
			System.out.println("tuesday");
		}
		else
		{
			System.out.println("Invalid week number");
		}

	}

}
